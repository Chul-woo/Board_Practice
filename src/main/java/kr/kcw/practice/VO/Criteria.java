package kr.kcw.practice.VO;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

public class Criteria {
	
	private static final Logger logger = LoggerFactory.getLogger(Criteria.class);
	
	private int page;	// 보여줄 페이지
	private int perPageNum;	// 페이지당 보여줄 페이지수
	
	//limit 구문에서 시작 부분에 필요한 값을 반환(mybatis에서 사용)
	public int getPageStart() {
		return (this.page - 1) * this.perPageNum;
	}
	
	public Criteria() {	// 최초 default 생성자로 기본 객체 생성 시 초기값 설정
		this.page = 1;	// 사용자가 세팅하지 않으면 1
		this.perPageNum = 10;	// 페이지당 기본 10개씩 출력하도록 세팅		
	}

	public int getPage() {
		return page;
	}

	public void setPage(int page) {
		if(page <= 0) {
			this.page = 1;
		}else {
			this.page = page;
		}
	}

	public int getPerPageNum() {
		return perPageNum;
	}

	public void setPerPageNum(int perPageNum) {
		logger.info("---------------페이지 표시 갯수 --------------");
		if(perPageNum <= 0 || perPageNum > 100) {
			this.perPageNum = 10;
		}else {
			this.perPageNum = perPageNum;
		}
	}

	@Override
	public String toString() {
		return "Criteria [page=" + page + ", perPageNum=" + perPageNum + "]";
	}

}
